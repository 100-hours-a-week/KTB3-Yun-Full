package domain;

public class Books {
    public String title;
    public String author;
    public String genre;
    public String publisher;
    public int pages;

    public Books(String title, String author, String genre, String publisher, int pages) {
        this.title = title;
        this.author = author;
        this.genre = genre;
        this.publisher = publisher;
        this.pages = pages;
    }
}


